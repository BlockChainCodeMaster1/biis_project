export async function whiteListMint(req, res) {
  let { address, txid } = req.body;

  if (!address || !txid) {
    res.send({
      msg: "Incomplete parameter",
      code: 0,
    });
    return;
  }

  const mint_number = await SOGA.count();

  if (mint_number >= 1500) {
    res.send({
      msg: "Whitelist Mint is overflow",
      code: 0,
    });
    return;
  }

  const is_minted = await SOGA.count({
    where: {
      address: address,
    },
  });

  if (is_minted > 0) {
    res.send({
      msg: "Already mint",
      code: 0,
    });
    return;
  }

  const result = await SOGA.create({
    address: address,
    tx: txid,
    is_whitelist: "true",
  });

  if (result) {
    res.send({
      msg: "Success",
      code: 1,
    });
  } else {
    res.send({
      msg: "Save failure",
      code: 0,
    });
  }
}
